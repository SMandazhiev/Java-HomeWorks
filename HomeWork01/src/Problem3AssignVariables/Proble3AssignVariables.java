package Problem3AssignVariables;

public class Proble3AssignVariables {

    public static void main(String[] args) {
        boolean boolvariable = false;
        String stingvariable = "Palo Alto, CA";
        short shortvariable = 32767;
        int integer32variable = 2000000000;
        double doublevariable = 0.1234567891011;
        float floatvariable = 0.5f;
        long longvariable = 919827112351L;
        byte bytevariable = 127;
        char charactervariable = 'c';

        System.out.println(boolvariable);
        System.out.println(stingvariable);
        System.out.println(shortvariable);
        System.out.println(integer32variable);
        System.out.println(doublevariable);
        System.out.println(floatvariable);
        System.out.println(longvariable);
        System.out.println(bytevariable);
        System.out.println(charactervariable);
    }
}
